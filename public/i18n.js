  function normalizeLocale(locale) {
    const raw = String(locale || "").trim();
    if (SUPPORTED.includes(raw)) return raw;
    const short = raw.slice(0, 2).toLowerCase();
    if (short === "tr") return "tr-TR";
    if (short === "ru") return "ru-RU";
    if (short === "ko") return "ko-KR";
    return "en-US";
  }

  function format(template, params = {}) {
    return String(template || "").replace(/\{(\w+)\}/g, (_, key) => params[key] ?? "");
  }

  function getCurrentLocale() {
    const current = window.__omnitutorUiLocale || localStorage.getItem("omnitutor-ui-locale") || navigator.language || "en-US";
    return normalizeLocale(current);
  }

  function t(key, params = {}, fallback = "") {
    const locale = getCurrentLocale();
    const dict = DICTS[locale] || DICTS["en-US"];
    const base = dict[key] ?? DICTS["en-US"][key] ?? fallback ?? key;
    return format(base, params);
  }

